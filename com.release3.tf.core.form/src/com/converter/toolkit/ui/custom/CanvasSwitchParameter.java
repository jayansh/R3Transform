package com.converter.toolkit.ui.custom;

public class CanvasSwitchParameter {
	String sourceForm;
	String sourceBlock;
	String sourceItem;
	String destinationForm;
	String destinationBlock;
	String destinationItem;

	public String getSourceForm() {
		return sourceForm;
	}

	public void setSourceForm(String sourceForm) {
		this.sourceForm = sourceForm;
	}

	public String getSourceBlock() {
		return sourceBlock;
	}

	public void setSourceBlock(String sourceBlock) {
		this.sourceBlock = sourceBlock;
	}

	public String getSourceItem() {
		return sourceItem;
	}

	public void setSourceItem(String sourceItem) {
		this.sourceItem = sourceItem;
	}

	public String getDestinationForm() {
		return destinationForm;
	}

	public void setDestinationForm(String destinationForm) {
		this.destinationForm = destinationForm;
	}

	public String getDestinationBlock() {
		return destinationBlock;
	}

	public void setDestinationBlock(String destinationBlock) {
		this.destinationBlock = destinationBlock;
	}

	public String getDestinationItem() {
		return destinationItem;
	}

	public void setDestinationItem(String destinationItem) {
		this.destinationItem = destinationItem;
	}
}
